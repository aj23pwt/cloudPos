package com.greencloud.entity;

public class PosDrinksDepositOrder extends OperationInfo {

	/**
	 * 
	 */
	private static final long serialVersionUID = -113657853558387983L;
	
	private String owner;
	private String phone;
	private String remark;
	private String state;
	
	@Override
	public String toString() {
		return "PosDrinksDepositOrder [owner=" + owner + ", phone=" + phone
				+ ", remark=" + remark + ", state=" + state + "]";
	}
	public String getOwner() {
		return owner;
	}
	public void setOwner(String owner) {
		this.owner = owner;
	}
	public String getPhone() {
		return phone;
	}
	public void setPhone(String phone) {
		this.phone = phone;
	}
	public String getRemark() {
		return remark;
	}
	public void setRemark(String remark) {
		this.remark = remark;
	}
	public String getState() {
		return state;
	}
	public void setState(String state) {
		this.state = state;
	}
}
